class WordFrequencyModel{
    private String word;
    private int repetation;
    
    public void setWord(String word){
        this.word = word;
    }
    
    public void setRepetation(int repetation){
        this.repetation = repetation;
    }
    
    public int getRepetation(){
        return repetation;
    }
    
    public String getWord(){
        return word;
    }
}
